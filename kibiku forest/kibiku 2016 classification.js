/*Define area of interest from the Kibiku forest shape file.*/
var AOIs = kibiku;

/*Define the cloud masking function for sentinel-2 imagery, this will help eliminate the 
  cloud pixels from the imagery/ image data to be processed.*/
function maskS2clouds(image){
  var qa = image.select('QA60');
  // Bits 10 and 11 are clouds and cirrus, respectively.
  var cloudBitMask = 1 << 10;
  var cirrusBitMask = 1 << 11;
  // Both flags should be set to zero, indicating clear conditions.
  var mask = qa.bitwiseAnd(cloudBitMask).eq(0)
      .and(qa.bitwiseAnd(cirrusBitMask).eq(0));
  return image.updateMask(mask).divide(10000);
}

/* Use the image collection to produce 
   a single cloud free sentinel image for year 2016 and clip it to the Kibiku forest area*/
var clipped = sentinel2.
              //Filter to the 2016 full year date range
              filterDate('2016-01-01','2016-12-31').
              //Filter the imagery to maintain only the meta-data for the kibiku area only
              filterBounds(AOIs).
              //Apply the cloud masking function to each of the remaining images in the filtered collection using the 'map()' function
              map(maskS2clouds).
              //use the 'median()' reduction function to reduce the image collection into a single image
              median().clip(AOIs);

//Let the map center to the Kibiku area to a zoom level of 12
Map.centerObject(AOIs,12);

//Image visualization
