/* The following section defines the code block for supervised classification of the 
    kibiku area imagery to the land cover classes.
    The classes are defined as follows with their numeric labels as follows below:
    
    0: Natural Forest
    1: Plantation Forest
    2: Bare Land
    3: Crop Land
    4: Open Grass Land
    5: Wooded Grass Land
    6: Settlement */
    
// The name for the property of the feature collection to be used for classification will be 'LC2'
var clasName2016 = 'LC2';

// The input bands for classification will be used as inputs for the classifier.
var bands = ['B2', 'B3', 'B4', 'B5', 'B6', 'B7', 'B8', 'B11', 'B12'];


/* Below training features are picked using the geometry drawing tools in the map display
    shown below the code editor. They are drawn around the identified land cover on the source image,
    and labelled using the numeric labels shown above, then merged into the training features*/
var training_points = (Natural_Forest_2).merge(Plantion_Forest_2).merge(Bareland_2).merge(crop_land_2).merge(Open_Grassland_2).merge(Wooded_Grassland_2).merge(Settlement_2);
print('#2016_Classification_new', training_points);

// Introduce a column in the training features that will give them random numbers between 0 and 1
var sample = training_points.randomColumn();

// Split the training features into two, with the number 0.7 as the mid-point
var split = 0.7;

// Pick the training set from the features with a random column value of less than 0.7
var trainingSP = sample.filter(ee.Filter.lt('random', split));
print(trainingSP)

//Sample the training data (sample data) from the training set
var training2016 = clipped.select(bands).sampleRegions({
  collection: trainingSP,
  properties: ['LC2'],
  scale: 10
});

/* Initialize and train a random forest classifier 'smileRandomForest' using the 'train()' function.
    the arguements for the train function are as follows in order:
    1. The sample data
    2. The name of the property of the training features containing the label for classification
    3. The classification input bands
    */
var trained2016 = ee.Classifier.smileRandomForest(10).train(training2016, clasName2016, bands);

//Classify the cloud free image using the 'classify()' function and pass the trained classifier as input.
var classified2016 = clipped.select(bands).classify(trained2016);

/*The classification process produces an image with a single band, the classification band. The image is divided into classified regions,
  whereby the pixels in each region have different values for the classification band. For each class, the image has pixel value
  corresponding to the classification value for that class.*/

//visualize the classified image.
